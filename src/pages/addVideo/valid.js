
export function validTitle (title){
    if (title.length >= 3){
        return false
    }
    else{
        return true
    }
}

export function validLink (link){
    if (link.length >= 3){
        return false
    }
    else{
        return true
    }
}

export function validImg(link){
    if (link.length >= 3){
        return false
    }
    else{
        return true
    }
}

export function validDescription (link){
    if (link.length >= 20){
        return false
    }
    else{
        return true
    }
}
